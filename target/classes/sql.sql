CREATE DATABASE IF NOT EXISTS CisolsMusic;

CREATE TABLE musics
(
  musicId INT NOT NULL AUTO_INCREMENT ,
  musicName VARCHAR(255) NOT NULL ,
  mSingerId INT NOT NULL ,
  mAlbumId INT,
  mUrl VARCHAR(255) NOT NULL,
  PRIMARY KEY(musicId)
)ENGINE=Innodb DEFAULT CHARSET=utf8 AUTO_INCREMENT = 1000;